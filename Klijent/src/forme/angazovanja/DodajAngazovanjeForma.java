    private void btnDodajAngazovanjeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajAngazovanjeActionPerformed
        try{
            Zaposleni zaposleni = (Zaposleni) cmbZaposleni.getSelectedItem();
            Projekat projekat = (Projekat) cmbProjekat.getSelectedItem();

            if (txtDatumPocetkaAngazovanja.getText().isEmpty()) {
                throw new Exception("Niste uneli pocetak angazovanja.");
            }
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
            Date datumPocetkaRealizacije = sdf.parse(txtDatumPocetkaAngazovanja.getText());
            
            Angazovanje angazovanje = new Angazovanje(0, projekat, zaposleni, datumPocetkaRealizacije, null);
            KlijentKontrolerAngazovanje.getInstanca().dodajAngazovanje(angazovanje);
            
            cmbZaposleni.setSelectedIndex(0);
            cmbProjekat.setSelectedIndex(0);
            txtDatumPocetkaAngazovanja.setText("");
            
            JOptionPane.showMessageDialog(rootPane, "Uspesno ste dodali angazovanje.");
            
        } catch (Exception ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
        
    }//GEN-LAST:event_btnDodajAngazovanjeActionPerformed


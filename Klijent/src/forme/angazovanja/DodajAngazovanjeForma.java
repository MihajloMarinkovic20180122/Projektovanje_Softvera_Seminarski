    private void popuniCmbs() throws Exception {
        cmbZaposleni.removeAllItems();
        LinkedList<Zaposleni> listaZaposlenih = KlijentKontrolerZaposleni.getInstanca().vratiZaposlene();
        for (Zaposleni zaposleni : listaZaposlenih) {
            cmbZaposleni.addItem(zaposleni);
        }
        
        cmbProjekat.removeAllItems();
        LinkedList<Projekat> litsaProjekata = KlijentKontrolerProjekat.getInstanca().vratiProjekte();
        for (Projekat projekat : litsaProjekata) {
            cmbProjekat.addItem(projekat);
        }
        
    }
